# Here We Go!
age = int(input("What is your age? "))

# The Functionality of "end" and "sep" in print function
print("Hello, Your age is ", end="\n")
print(age)
print("Next year, you will be ", end="")
print(age + 1)
print("You are", age, sep=" ")

# How to print quotes in python
print("Hello, \"friend\"")
print('Hello, "friend"')